let grow = 0;
var clutter = "";
arr.forEach(function(elem, idx){
    clutter += `<div class="story">
    <img id = "${idx}"src="${elem.dp}" alt="Error">
</div>`;
})

document.querySelector("#storiyan").innerHTML = clutter;
document.querySelector("#storiyan").addEventListener("click", function(data){
    document.querySelector("#full-scr").style.display = "initial";
    document.querySelector("#full-scr").style.backgroundImage = `url(${arr[data.target.id].img})`;

    setTimeout(()=>{
        document.querySelector("#full-scr").style.display = "none"
    }, 3000)

    if (grow<100) {
        setInterval(()=>{
            document.querySelector("#growth").style.width = `${grow}%`
            grow++
        },30)
    }else{
        grow = 0
    }
    
})